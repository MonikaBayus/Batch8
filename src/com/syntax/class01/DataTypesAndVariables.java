package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		
		//Primitive Data Types
		//Numeric
		//type name = assign value
		
		byte variable1 = 4; //127 to -128
		
		short variable2 = 10; //32767 to -32768
		
		int variable3 = 100; 
		
		long variable4 = 100000L; //128348924L
		
		//to store floating numbers
		
		float variable5 = 12.99f;
		double variable6 = 9.99999;	
		
		
		//True or False - Boolean
		
		boolean variable7 = true;
		boolean variable8 = false;
		
		//to represent different character
		
		char variable9 = 'C';
		char variable10 = '2';
		char variable11 = '&';
		
		
			System.out.print(variable7);
			System.out.println();
			System.out.println(variable10);
	}

}
